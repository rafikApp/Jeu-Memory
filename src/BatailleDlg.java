    /**
     * @param args the command line arguments
     */
    public void initListJ() {
        DefaultListModel mod = new DefaultListModel(); 
        ListJ.setModel(mod);
        for(int i=0;i<lj.getNbJoueurs();i++){
            mod.addElement(lj.getJoueur(i).getPseudo());
            ListJ.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        }
    }
    private void AnnulerActionPerformed(java.awt.event.ActionEvent evt) {
        this.ok=false; // Affecter false a l'attribut ok
        if (Annuler.getText().equals("Annuler"))
            this.b.setDeroulement("Bataille interrompue en cours de partie");
        this.setVisible(false);
        this.dispose();
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    



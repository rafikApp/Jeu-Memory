    private void ListJMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListJMouseClicked
        // TODO add your handling code here:
        int ind = ListJ.getSelectedIndex();  // Récuperation de l'indice du joueur séléctionné
        if(ind!=-1){  
            Joueur jo = this.lj.getJoueur(indj);  // Récuperation du joueur courant
            if(this.lj.getJoueur(ind).getPseudo().equals(jo.getPseudo())) // Si le pseudo du joueur séléctionné est celui du joueur courant
                Joueur.setText("Selectionner un joueur different"); // Affichage d'un message
            else { // si non 
                this.adversaire = lj.getJoueur(ind); // Récuperation du joueur séléctionné
                b = new Bataille(jo, this.adversaire); // Création d'un objet de type Bataille en appelant le constructeur de la classe Bataille qui prend en paramètres j courant, j séléctionné(adversaire).
                Annuler.setVisible(true); // Rendre le bouton annuler visisble
                Joueur.setText(""+jo.getPseudo()); // Affichege du pseudo du joueur 
                Adversaire.setText(this.adversaire.getPseudo()); // Affichege du pseudo d'adversaire
                InfosCarte1.setText(jo.getPaquet().toString());  // Affichage du paquet du joueur 
                InfosCarte2.setText(this.adversaire.getPaquet().toString()); // Affichage du paquet d'adversaire 
            }
        }
    }//GEN-LAST:event_ListJMouseClicked

    private void DemarrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DemarrerActionPerformed
        // TODO add your handling code here:
        Joueur jc = lj.getJoueur(indj);  // Récuperation du joueur courant
        InfosCarte1.append("\n"+jc.getPseudo()+" joueur "+jc.getPaquet().getPerso(0)); // Ajout d'un texte a la zone d'édition (InfosCarte1)
        InfosCarte2.append("\n"+this.adversaire.getPseudo()+" joueur "+this.adversaire.getPaquet().getPerso(0)+"\n"); // Ajout d'un texte a la zone d'édition (InfosCarte2)
        int res = b.execute(); // récuperation de l'entier renvoyer par la methode execute de la classe bataille
        switch(res){ // Les instruction a effectuer selon la valeur de la variable res
            case 0 : InfosCarte1.append("ex-aequo aucun gagnant \n");
                     InfosCarte2.append("ex-aequo aucun gagnant \n");
                     break;
            case 1 : InfosCarte1.append(jc.getPseudo()+" gagne \n ");
                     InfosCarte2.append(this.adversaire.getPseudo()+" perdu \n ");
                     Vainqueur.setText(jc.getPseudo());
                     break;
            case 2 : InfosCarte1.append(jc.getPseudo()+" perdu \n ");
                     InfosCarte2.append(this.adversaire.getPseudo()+" gagne \n ");
                     Vainqueur.setText(this.adversaire.getPseudo());
                     break;         
        }
        InfosCarte1.append(jc.getPaquet().toString());
        InfosCarte2.append(this.adversaire.getPaquet().toString());
        ok = true; // Affecter true a l'attribut ok
        LesPersonnages pjc = jc.getPaquet(); // Recuperation du paquet du joueur courant
        LesPersonnages pad = this.adversaire.getPaquet(); // Recuperation du paquet d'adversaire
        if(pjc.getTaille()==0 || pad.getTaille()==0){  // Si au moin la taille d'un des deux paquet est nul
            Demarrer.setEnabled(false); // Désactiver le bouton Demarrer
            Annuler.setText("Fermer"); // Changer le text du bouton Annuler a Fermer
        }
        else { // si non 
            pjc.getPerso(0).setImgBouton(Carte1); // Affichage de la première carte du paquet du joueur courant dans le bouton Carte1
            pad.getPerso(0).setImgBouton(Carte2); // Affichage de la première carte du paquet d'adversaire dans le bouton Carte2
        } 
    }//GEN-LAST:event_DemarrerActionPerformed
